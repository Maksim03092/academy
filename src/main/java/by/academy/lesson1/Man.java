package by.academy.lesson1;

public class Man {

    private String name;
    private int age;

    public int grow(){
        return age = age + 1;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
